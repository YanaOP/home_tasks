<?php
/**
Создать массив размерности 20 и заполнить случайными величинами от 1 до 3
Вывести на экран сумму индексов где значение равно 1
То есть если есть массив в котором по индексам 3 7 10 стоит значение один, на выходе я получу число 20 (3 + 7 + 10)
 */
$arr = array();
$index_sum = 0;
for($i = 0; $i < 20 ; $i++){
    $arr[$i] = rand(1,3);
    if ($arr[$i] == 1) {
        $index_sum += $i;
    }

} print_r($arr); echo '<br>'.$index_sum;