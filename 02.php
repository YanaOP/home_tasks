<?php
/**
Обьявить две случаных случайных переменных $a , $b
Вывести на экран значение переменных и в случае если
$a больше чем $b вывести на экран "$a больше"
$b больше чем $a вывести на экран «$b больше»
Если они равны вывести на экран "$a равно $b»
 */
